const SAAS_METERING_JS = `// metering.js
// Azure Marketplace Metering Service - emit usage events for metered (pay-as-you-go) dimensions.
// Docs: https://learn.microsoft.com/partner-center/marketplace/marketplace-metering-service-apis
import { ClientSecretCredential } from '@azure/identity';

// Fixed resource/audience for the commercial marketplace metering API.
const MARKETPLACE_SCOPE = '20e940b3-4c77-4b0b-9a53-9e16a1b010a7/.default';
const USAGE_EVENT_URL =
  'https://marketplaceapi.microsoft.com/api/usageEvent?api-version=2018-08-31';

const credential = new ClientSecretCredential(
  process.env.AZURE_TENANT_ID,
  process.env.AZURE_CLIENT_ID,
  process.env.AZURE_CLIENT_SECRET
);

async function getToken() {
  const token = await credential.getToken(MARKETPLACE_SCOPE);
  if (!token) throw new Error('Could not acquire a marketplace access token.');
  return token.token;
}

/**
 * Emit a single metered usage event.
 * @param {object} evt
 * @param {string} evt.resourceId  SaaS subscription id (or managed-app resourceUsageId)
 * @param {string} evt.planId      Plan the customer is on
 * @param {string} evt.dimension   Metered dimension id from your plan (e.g. "api_calls")
 * @param {number} evt.quantity    Units consumed since the last event
 */
export async function emitUsageEvent({ resourceId, planId, dimension, quantity }) {
  const accessToken = await getToken();
  const res = await fetch(USAGE_EVENT_URL, {
    method: 'POST',
    headers: {
      Authorization: \`Bearer \${accessToken}\`,
      'Content-Type': 'application/json'
    },
    body: JSON.stringify({
      resourceId,
      planId,
      dimension,
      quantity,
      effectiveStartTime: new Date().toISOString()
    })
  });

  if (!res.ok) {
    throw new Error(\`Usage event failed: \${res.status} \${await res.text()}\`);
  }
  return res.json();
}

/**
 * Batch up to 25 usage events in a single call.
 * Docs: .../marketplace-metering-service-apis#metered-billing-batch-usage-event
 */
export async function emitBatchUsage(events) {
  const accessToken = await getToken();
  const res = await fetch(
    'https://marketplaceapi.microsoft.com/api/batchUsageEvent?api-version=2018-08-31',
    {
      method: 'POST',
      headers: {
        Authorization: \`Bearer \${accessToken}\`,
        'Content-Type': 'application/json'
      },
      body: JSON.stringify({ request: events })
    }
  );
  if (!res.ok) {
    throw new Error(\`Batch usage failed: \${res.status} \${await res.text()}\`);
  }
  return res.json();
}
`;

