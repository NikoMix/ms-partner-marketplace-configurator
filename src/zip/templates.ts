function addBillingFiles(folder: JSZip, ctx: BillingZipContext): void {
  folder.file('README.md', billingReadme(ctx));
  folder.file('plans.json', plansJson(ctx));

  switch (ctx.billingZipKind) {
    case 'saas-metered':
      addSaasMetered(folder);
      break;
    case 'saas-subscription':
      addSaasSubscription(folder);
      break;
    case 'azure-app':
      addAzureApp(folder);
      break;
    case 'azure-vm':
      addAzureVm(folder);
      break;
    case 'azure-container':
      addAzureContainer(folder);
      break;
    case 'marketplace-generic':
      addGeneric(folder);
      break;
    case 'none':
      addGeneric(folder);
      break;
  }
}

export async function buildBillingZip(ctx: BillingZipContext): Promise<Blob> {
  const zip = new JSZip();
  const root = zip.folder('billing-starter');
  if (root) addBillingFiles(root, ctx);
  return zip.generateAsync({ type: 'blob' });
}

export interface ProjectAsset {
  filename: string;
  dataUrl: string;
}

export interface ProjectZipContext {
  plan: unknown;
  listingMarkdown: string;
  assets: ProjectAsset[];
  billing?: BillingZipContext;
}

export async function buildProjectZip(ctx: ProjectZipContext): Promise<Blob> {
  const zip = new JSZip();
  zip.file('plan.json', JSON.stringify(ctx.plan, null, 2));
  zip.file('listing.md', ctx.listingMarkdown);

  if (ctx.assets.length) {
    const assetsFolder = zip.folder('assets');
    if (assetsFolder) {
      for (const a of ctx.assets) {
        const base64 = a.dataUrl.split(',')[1];
        if (base64) assetsFolder.file(a.filename, base64, { base64: true });
      }
    }
  }

  if (ctx.billing) {
    const billingFolder = zip.folder('billing');
    if (billingFolder) addBillingFiles(billingFolder, ctx.billing);
  }

  return zip.generateAsync({ type: 'blob' });
}
